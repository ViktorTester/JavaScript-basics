                                        // Ввод данных


// Для того чтобы запросить входные данные от пользователя в заданиях
// этого курса, мы будем использовать команду prompt()

// prompt() - это функция, которая используется для получения информации от пользователя.
// Любые данные, полученные от пользователя через prompt() относятся к типу Строка.

// Значение, полученное от пользователя с помощью prompt(), можно присвоить переменной.

// Пример:
let username = prompt("Как вас зовут?");

// Когда вы запустите этот код в браузере, он покажет диалоговое окно с вопросом
// "Как вас зовут?". После того как пользователь введет своё имя и нажмет "ОК",
// введенное значение будет сохранено в переменной username,
// которую вы можете использовать как обычную переменную.

// Обратите внимание, что формулировка вопроса может быть любой или вопроса может
// не быть вообще. Вопрос никак не повлияет на правильность решения задачи в
// заданиях этого курса, поэтому можно его не писать.

// Если в задании вам нужно запросить ввод более одной строки,
// используйте prompt() соответствующее количество раз:
const yourName = prompt("Как вас зовут?");
const yourAge = prompt("Сколько вам лет?");


