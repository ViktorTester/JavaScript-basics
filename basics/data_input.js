// JavaScript не содержит стандартных методов ввода. Поэтому prompt() будет работать,
// только если вы выберете запустить JavaScript код в заданиях этого курса,
// с помощью веб-страницы, в консоли, а также в некоторых онлайн-интерпретаторах.

// Для того, чтобы реализовать ввод данных в среде Node.js
// вам понадобится использовать следующий блок кода:
const readline = require('readline').createInterface({

    input: process.stdin,
    output: process.stdout

});

readline.question('Как вас зовут?', name => {

    console.log(name);
    readline.close();

});