// В заданиях этого курса вам часто нужно будет запросить числовые данные.
// Для этого мы будем использовать следующую команду: Number(prompt()).

// Number() - это функция, которая позволяет преобразовать данные в число.

// Пример:
const num = Number(prompt("Введите число"));

// Если в задании вам нужно запросить ввод двух или более чисел,
// используйте Number(prompt()) соответствующее количество раз:
const thisYear = Number(prompt("Введите текущий год"));
const nextYear = Number(prompt("Введите следующий год"));

